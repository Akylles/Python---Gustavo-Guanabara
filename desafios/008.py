metros = float(input('Digite a distância em metros: '))

quilometros = metros / 1000
centimetros = metros * 100
milimetros = metros * 1000

print('Metros: {:.2f}'.format(metros))
print('Quilômetros: {:.2f}'.format(quilometros))
print('Centímetros: {:.2f}'.format(centimetros))
print('Milímetros: {:.2f}'.format(milimetros))